    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        // TODO add your handling code here:
         if (bancobbdd.checkPassword(tfNIF.getText(), tfPassword.getText()) && !bancobbdd.isDisabled) {
            EndUser fu = new EndUser();
            fu.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "User and password do not match.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginMouseClicked

    private void loginadminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginadminMouseClicked
        // TODO add your handling code here:
         if (bancobbdd.checkPassword(tfNIF.getText(), tfPassword.getText()) && bancobbdd.isAdmin) {
            System.out.println("Valido y admin");
            Admin adPanel = new Admin();
            adPanel.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error user and password don't match nor you aren't admin.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginadminMouseClicked

