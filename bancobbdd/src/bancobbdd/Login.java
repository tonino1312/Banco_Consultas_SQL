    private void initDef() {
        pStatus.setPreferredSize(new Dimension(10, 10));
        
        if(bancobbdd.conn2 != null && bancobbdd.conn1 != null){
            pStatus.setBackground(Color.green);
            lStatus.setForeground(Color.green);
            pStatus.setToolTipText("All working good");
            lStatus.setText("All working");
        } else if(bancobbdd.conn2 == null && bancobbdd.conn1 == null){
            pStatus.setBackground(Color.red);
            lStatus.setForeground(Color.red);
            lStatus.setText("Couldn't connect to any DB");
        } else {
            if(bancobbdd.conn1 == null){
                pStatus.setToolTipText("Cloud working, local doesn't");
                pStatus.setBackground(Color.green);
                lStatus.setForeground(Color.green);
                lStatus.setText("Cloud working, local doesn't");
            } else {
                pStatus.setBackground(Color.orange);
                lStatus.setForeground(Color.orange);
                pStatus.setToolTipText("Couldn't connect to external DB, try to not use local");
                lStatus.setText("Couldn't connect to external DB, try to not use local");
            }
        }
    }

   

